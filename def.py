def somar (a, b):
    resultado = a + b
    return resultado
soma_de_5_e_3 = somar(5, 3)
print(f"A soma de 5 e 3 é: {soma_de_5_e_3}")

soma_de_10_e_20 = somar(10, 20)
print(f"A soma de 10 e 20 é: {soma_de_10_e_20}")

print(f"A soma de 5 e 3 é: {soma_de_5_e_3} e a soma de 10 e 20 é: {soma_de_10_e_20}")